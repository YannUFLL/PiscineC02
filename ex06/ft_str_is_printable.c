int	ft_str_is_printable(char *str)
{
	int	i;
	int	c;

	i = 0;
	c = 1;
	if (str[i] == '\0')
		return (1);
	else
	{
		while (str[i] != '\0')
		{
			if (((str[i] >= 1) && (str[i] <= 31)) || (str[i] != 127))
			{
				i++;
				c = 1;
			}
			else
			{
				c = 0;
				return (c);
				break ;
			}
		}
		return (c);
	}
}
