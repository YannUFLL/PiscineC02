char	*ft_strcapitalize(char *str)
{
	int	i;
	int	c;

	i = 0;
	c = 1;
	while (str[i] != '\0')
	{
		while (str[i] == 32)
			i++;
		if (((str[i] >= 97) && (str[i] <= 122)))
		{
			str[i] = str[i] - 32;
		}
		while ((str[i] != 32) && (str[i] != 0))
		{
			if ((str[i] == 43) || (str[i] == 45))
				break ;
			i++;
		}
		i++;
	}
	return (str);
}
