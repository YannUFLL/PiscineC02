char	*ft_strlowcase(char *str)
{
	int	i;
	int	c;

	i = 0;
	c = 1;
	while (str[i] != '\0')
	{
		if (((str[i] >= 65) && (str[i] <= 90)))
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
